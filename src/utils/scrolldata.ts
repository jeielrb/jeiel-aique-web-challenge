interface Blog {
  id: number;
  title: string;
  description: string;
  image: { uri: string };
  url: string;
}

export function formatBlogs(
  blogs: Blog[]
): {
  id: number;
  title: string;
  description: string;
  image: string;
  url: string;
}[] {
  return blogs.map((blog) => ({
    id: blog.id,
    title: blog.title,
    description: blog.description,
    image: blog.image.uri,
    url: blog.url,
  }));
}
